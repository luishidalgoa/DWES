<?php
    // Archivo: funciones.inc.php

    // Función para saludar
    function restar($a, $b) {
        return $a - $b;
    }

    // Función para sumar dos números
    function sumar($a, $b) {
        return $a + $b;
    }

    // Función para obtener el mayor de dos números
    function obtenerMayor($a, $b) {
        return ($a > $b) ? $a : $b;
    }
?>